function finnTall = Romertall( arg )
    switch ( arg )
    case 0
    finnTall = '';    
    case 1
    finnTall = 'I';
    case 2
    finnTall = 'II';
    case 3
    finnTall = 'III';
    case 4
    finnTall = 'IV';
    case 5
    finnTall = 'V';
    case 6
    finnTall = 'VI';
    case 7
    finnTall = 'VII';
    case 8
    finnTall = 'VIII';
    case 9
    finnTall = 'IX';
    case 10
    finnTall = 'X';
    otherwise
    finnTall = 'Ikke definert';
    end
end

